#the intersection of two sets is the set of elements that are common to both sets.
#The intersection() method returns a new set containing only the elements that are common to both sets.

ItalianFood = {"Pizza", "Pasta", "Lasagna", "Risotto"}
JapaneseFood = {"Sushi", "Tempura", "Ramen", "Risotto"}

# Intersection of two sets
commonFood = JapaneseFood.intersection(ItalianFood)
print(commonFood)
 